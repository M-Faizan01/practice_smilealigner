                        <li>
                            What Is Smilealign and How Does It Work?
                            <ul>
                                <li>
                                    What are aligners made of, and what do they look like?
                                    <ul>
                                        <li>
                                            <p>The virtually invisible aligners, which are made of a thermoplastic
                                                material uniquely developed for the Smilealign treatment plan, look
                                                similar to teeth-whitening trays. A series of Smilealign aligners are
                                                custom-made for you, to move your teeth in the sequence determined by
                                                your doctor.</p>
                                        </li>
                                    </ul>
                                </li>
                                <li>
                                    How do the aligners straighten teeth?
                                    <ul>
                                        <li>
                                            <p>Over the course of treatment, you will be supplied with a series of
                                                aligners. Each aligner will make slight adjustments to tooth position, a
                                                process that is mapped out in advance by your doctor, specifically for
                                                your situation. When the aligners are placed on the teeth, they cause
                                                the teeth to gradually shift from their current position. After
                                                approximately two weeks, you will begin using the next set of aligners,
                                                which will continue the teeth straightening process.</p>
                                        </li>
                                    </ul>
                                </li>
                                <li>
                                    How will Smilealign effectively move my teeth?
                                    <ul>
                                        <li><p>Through the use of our patented thermoplastic design, Smilealign aligners
                                                move your teeth through the appropriate placement of controlled force.
                                                The main difference is that Smilealign not only controls the force, but
                                                also the timing of the force application. During each stage, only
                                                certain teeth are allowed to move. These movements are determined by
                                                your doctor as he or she maps out your unique Smilealign treatment
                                                plan.</p></li>
                                    </ul>
                                </li>
                                <li>
                                    What are the primary benefits of Smilealign?
                                    <ul>
                                        <li><p>Smilealign aligners are virtually invisible. No one may even notice that
                                                you're wearing them, making Smilealign a seamless fit with your
                                                lifestyle. Smilealign aligners are removable. For the best results and a
                                                timely outcome, aligners should be worn for 20 to 22 hours per day.
                                                However, unlike with braces, you have the flexibility to remove your
                                                aligners to eat and drink what you want during treatment. And you can
                                                also remove the aligners to brush and floss as you normally would, for
                                                fresh breath and good oral hygiene. There are no metal brackets or wires
                                                that could cause irritation to your mouth, an advantage over traditional
                                                braces. Plus, since your office visits during treatment don't involve
                                                metal or wire adjustments, you'll likely spend less time in the doctor's
                                                chair. Smilealign allows you to view your virtual results and treatment
                                                plan before you start so you can see how your straightened teeth will
                                                look when your treatment is complete. Instead of imagining how much
                                                better it can be, you'll be able to see it for yourself.</p></li>
                                    </ul>
                                </li>
