                                <li>
                                    My provider mentioned "attachments". What are they and why would I need them for my
                                    Smilealign treatment?
                                    <ul>
                                        <li>
                                            <p>
                                                Depending on your specific treatment, some of the movements may require
                                                either "attachments" or "ridges" to help the aligner grip the teeth.
                                                These bumps, or wells, are where the aligner grips the attachment the
                                                doctor places on your teeth. The attachments are actually small pieces
                                                of enamel-colored composite the doctor affixes to your teeth that are
                                                then gripped by bumps on your aligners. The ridges are slim indentations
                                                in your aligners. Your doctor will use one or both of these features to
                                                attempt to achieve the desired tooth movement.
                                            </p>
                                        </li>
                                    </ul>
