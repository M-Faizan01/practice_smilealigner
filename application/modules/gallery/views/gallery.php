        $('.nav-tabs .nav-filter').on('click',function() {
            var selector = $(this).attr('data-filter');
            $('.nav-tabs .nav-filter').removeClass('active');
            $(this).addClass('active');
            $container.isotope({ filter: selector });
            return false;
        });
